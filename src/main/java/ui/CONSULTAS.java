    private void comboDepasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboDepasItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            MDepartamentos dep = (MDepartamentos) comboDepas.getSelectedItem();
            DefaultTableModel modelo = (DefaultTableModel) tablaTareas.getModel();
            modelo.setRowCount(0);
            UsuarioDAO user = new UsuarioDAO();
            user.verTareasDepartamento(dep.getId(), 1, modelo);
        }
    }//GEN-LAST:event_comboDepasItemStateChanged

    private void comboUsuariosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboUsuariosItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            MUsuarios us = (MUsuarios) comboUsuarios.getSelectedItem();
            DefaultTableModel modelo = (DefaultTableModel) tablaTareas.getModel();
            modelo.setRowCount(0);
            UsuarioDAO user = new UsuarioDAO();
            user.misTareas(1, us.getId(), modelo);
        }
    }//GEN-LAST:event_comboUsuariosItemStateChanged

    private void comboMaxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboMaxItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            DefaultTableModel modelo = (DefaultTableModel) tablaMax.getModel();
            modelo.setRowCount(0);
            UsuarioDAO user = new UsuarioDAO();
            int id = comboMax.getSelectedIndex();
            user.busquedaMayor(id, modelo);
            if (id == 1) {
                mAnuncioTareas.setText("USUARIO QUE TIENE MAS TAREAS ASIGNADAS Y EN ESTADO ACTIVA");
            } else if (id == 2) {
                mAnuncioTareas.setText("USUARIO QUE TIENE MAS TAREAS VENCIDAS (TAREAS EN ESTADO ACTIVA Y FECHA DE ASIGNACION MAYOR A 15 DIAS)");
            }
        }
    }//GEN-LAST:event_comboMaxItemStateChanged

