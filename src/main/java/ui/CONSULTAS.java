    public void cargarMisTareas() {
        DefaultTableModel modelo = (DefaultTableModel) tablaTareas.getModel();
        modelo.setRowCount(0);
        UsuarioDAO user = new UsuarioDAO();
        user.misTareas(1, usuario.getId_usuario(), modelo);
    }

    public final void habilitarPermisos() {
        if (usuario.getId_tipo() != 1) {
            if (usuario.getId_tipo() == 3) {
                jLabel3.setVisible(false);
                jLabel4.setVisible(false);
                comboUsuarios.setVisible(false);
                comboMax.setVisible(false);
                bconsultausuario.setVisible(false);
                bconsultamayor.setVisible(false);
            }
            jLabel1.setVisible(false);
            comboDepas.setVisible(false);
            bconsultadepa.setVisible(false);
            jLabel4.setVisible(false);
            comboMax.setVisible(false);
            bconsultamayor.setVisible(false);
        }
        comboDepas.setEnabled(false);
        comboUsuarios.setEnabled(false);
        comboMax.setEnabled(false);
    }

    public final void cargarComboBox() {
        //cargar al combo box de departamentos
        MDepartamentos dep = new MDepartamentos();
        DefaultComboBoxModel modeloDep = new DefaultComboBoxModel(dep.mostrarDepas());
        comboDepas.setModel(modeloDep);

        //cargar al combo box de usuarios
        MUsuarios u = new MUsuarios();
        DefaultComboBoxModel modeloUs = new DefaultComboBoxModel(u.mostrarUsuarios(usuario.getId_tipo(), usuario.getId_departamento()));
        comboUsuarios.setModel(modeloUs);

        //cargar al combo box de Maximos
        IndiceMax in = new IndiceMax();
        DefaultComboBoxModel modeloIn = new DefaultComboBoxModel(in.mostrarIndices());
        comboMax.setModel(modeloIn);
    }

