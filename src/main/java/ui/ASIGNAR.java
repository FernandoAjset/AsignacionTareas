        public final void cargar() {
        DefaultTableModel modelo = (DefaultTableModel) TablaUsuarios.getModel();
        modelo.setRowCount(0);
        UsuarioDAO user = new UsuarioDAO();
        user.verSubs(usuario, modelo);
    }

    public void addTarea() {
        UsuarioDAO user = new UsuarioDAO();
        if (user.asignarTarea(descripcion, idSelec)) {
            JOptionPane.showConfirmDialog(null, "Tarea asignada Exitosamente", "Ejecuci√≥n correcta", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            desTarea.setText("");
            descripcion = "";
        } else {
            JOptionPane.showConfirmDialog(null, "Algo salio mal", "Error", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void TablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaUsuariosMouseClicked
        int seleccionar = TablaUsuarios.rowAtPoint(evt.getPoint());
        String aux = String.valueOf(TablaUsuarios.getValueAt(seleccionar, 0));
        idSelec = Integer.parseInt(aux);
        infUsuario.setText("Seleccionado: " + String.valueOf(TablaUsuarios.getValueAt(seleccionar, 2)));
    }//GEN-LAST:event_TablaUsuariosMouseClicked

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        descripcion = desTarea.getText();
        if (descripcion.equals("")) {
            JOptionPane.showConfirmDialog(null, "Ingrese descripcion", "Error", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
        } else if (idSelec == 0) {
            JOptionPane.showConfirmDialog(null, "Seleccione un usuario", "Error", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
        } else {
            addTarea();
        }
    }//GEN-LAST:event_guardarActionPerformed

